float getVoltage(){
	uint32_t adc_val;
	HAL_ADC_Start(&hadc1);
	if(HAL_ADC_PollForConversion(&hadc1, 10)==HAL_OK){
		adc_val=HAL_ADC_GetValue(&hadc1);

	}
	else {
		return 0;
	}
	HAL_ADC_Stop(&hadc1);
	adc_val*=voltageConversion;


	return adc_val;


}

